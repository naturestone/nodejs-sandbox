CREATE TABLE `USER` ( 
    `ID` INTEGER PRIMARY KEY AUTOINCREMENT UNIQUE, 
    `NAME` TEXT, 
    `DESC` TEXT, 
    `ACTIVE` INTEGER 
);

CREATE TABLE `ROLES` (
	`ID`	INTEGER PRIMARY KEY AUTOINCREMENT,
	`NAME`	TEXT UNIQUE,
	`DESC`	TEXT,
	`PARENT`	INTEGER
);

CREATE TABLE `MAP_ROLEMEMBER` (
	`ID`	INTEGER PRIMARY KEY AUTOINCREMENT,
	`ROLE_ID`	INTEGER,
	`USER_ID`	INTEGER,
	`ACTIVE`	INTEGER
);